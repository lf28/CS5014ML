function knn_predict(test_x ; train_x=mnist_train_X, train_y = mnist_train_ys, k = 1)
	n_test = size(test_x)[3]
	test_y = zeros(Int, n_test)
	for i in 1:n_test
		dv = sum((test_x[:,:,i] .- train_x).^2; dims=1:2)[:]
		# _, i_min = findmin(sum((test_x[:,:,i] .- train_x).^2; dims=1:2)[:])
		i_mins = partialsortperm(dv, 1:k)
		test_y[i] = train_y[i_mins] |> mode
	end
	return test_y
end;

